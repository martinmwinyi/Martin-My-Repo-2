# Primitive Data Types:
# String - denotated with quotes
name='Dylan'

# Int - integer ALl WHOLE numbers, positiv or negative
age=29

# Float - double - decimal point numbers
dream_hourly_rate=150.75

# Boolean - Bool - True/False - no quotes, must be capitalized
isAwake=True

# None - DataType - represent null or nothing, no value at all
money_in_bank=None

# Print statement allows us to PRINT values to the terminal
print(name)
print(age)
print(dream_hourly_rate)
print(isAwake)
print(money_in_bank)

